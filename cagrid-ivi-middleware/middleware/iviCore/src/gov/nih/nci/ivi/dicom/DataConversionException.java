package gov.nih.nci.ivi.dicom;

/**
 * @author tpan
 *
 */
public class DataConversionException extends Exception {



	public DataConversionException() {
		super();
	}

	public DataConversionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DataConversionException(String arg0) {
		super(arg0);
	}

	public DataConversionException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -6677944066859289612L;
}
