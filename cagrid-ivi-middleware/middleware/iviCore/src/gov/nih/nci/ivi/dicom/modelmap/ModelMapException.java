public class ModelMapException extends Exception {



	public ModelMapException() {
		super();
	}

	public ModelMapException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ModelMapException(String arg0) {
		super(arg0);
	}

	public ModelMapException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -6677944066859289612L;
}
